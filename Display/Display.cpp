//ユックリモード種別をアルファベットで取得
char Display::get_char_mode(int mode){
    char c;
    switch(mode){
        case SPEED_HIGH:
            c = 'K';
            break;
        case SPEED_LOW:
            c = 'Y';
            break;
        case SPEED_HYPER:
            c = 'H';
            break;
        case SPEED_DAMP:
            c = 'D';
            break;
    }
    
    return c;
}

