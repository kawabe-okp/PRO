void Display::createChar(uint8_t location, uint8_t charmap[]) {
  location &= 0x7; // we only have 8 locations 0-7
  writeCommand(0x40 | (location << 3));
  for (int i=0; i<8; i++) {
    writeData(charmap[i]);
  }
}

