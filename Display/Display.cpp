/*****************************************************************************
    >(アロウ)のアニメーション
*****************************************************************************/
void Display::ArrowMosion()
{
    static int i = 0;
    //最大表示幅を超えたら初期化
    if(i >= columns()){
        i = 0;
        blank(1);
    }
    
    putc('>');
    wait(_jm);
    i++;
}

